public class Maths {
    public static void main(String[] args){
        System.out.println("Maximum Number : "+Math.max(56,98));
        System.out.println("Minimum Number : "+Math.min(0,-1));
        System.out.println("Square root of a number : "+Math.sqrt(3));
        System.out.println("power of any number : "+Math.pow(7,4));
        System.out.println("Absolute value : "+Math.abs(-3));
        System.out.println("Radian value : "+Math.toRadians(180));
        


    }
    
}
